#include "simpleCalculations.h"

/**
 * This function is used to do the addition command.
 * @param arg1: user inputted value
 * @param arg2: user inputted value
 * @return returnValue: result of operation on user inputted values
 */
double addition(double arg1, double arg2){
    double returnValue = arg1 + arg2;
    return returnValue;
}

/**
 * This function is used to do the subtraction command.
 * @param arg1: user inputted value
 * @param arg2: user inputted value
 * @return returnValue: result of operation on user inputted values
 */
double subtraction(double arg1, double arg2){
    double returnValue = arg1 - arg2;
    return returnValue;
}

/**
 * This function is used to do the multiplication command.
 * @param arg1: user inputted value
 * @param arg2: user inputted value
 * @return returnValue: result of operation on user inputted values
 */
double multiplication(double arg1, double arg2) {
    double returnValue = arg1 * arg2;
    return returnValue;
}

/**
 * This function is used to do the division command.
 * @param arg1: user inputted value
 * @param arg2: user inputted value
 * @return returnValue: result of operation on user inputted values
 */
double division(double arg1, double arg2){
    double returnValue = arg1 / arg2;
    return returnValue;
}
